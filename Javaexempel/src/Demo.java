class Demo
{
    public static void main (String[] args)
    {
        // två heltal
        int    m = 5;
        int    n = 12;

        // heltalens produkt
        int    p = m * n;

        // visa heltalen och deras produkt
        System.out.println ("heltalen: " + m + ", " + n);
        System.out.println ("deras produkt: " + p);
    }
}

